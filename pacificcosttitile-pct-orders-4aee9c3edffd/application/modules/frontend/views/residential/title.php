					<h3 class="typography-title">What is Title Insurance?</h3>
					<p>Title insurance protects real estate owners and lenders against any property loss or damage they
						might experience because of liens, encumbrances or defects in the title to the property. Each
						title insurance policy is subject to specific terms, conditions and exclusions.</p><br>

					<h3 class="typography-title">Title Insurance vs. Traditional Insurance</h3>
					<p>Insurance such as car, life, health, etc., protects against potential future events and is paid
						for with monthly or annual premiums. A title insurance policy insures against events that
						occurred in the past of the real estate property and the people who owned it, for a one-time
						premium paid at the close of the escrow.</p><br>

					<h3 class="typography-title">What does Title Insurance Cover?</h3>
					<p>Title insurance protects against claims from defects. Defects are things such as another person
						claiming an ownership interest, improperly recorded documents, fraud, forgery, liens,
						encroachments, easements and other items that are specified in the insurance policy.</p><br>

					<h3 class="typography-title">Who needs Title Insurance?</h3>
					<p>Purchasers and lenders need title insurance in order to be insured against various possible title
						defects. The buyer, seller and lender all benefit from issuance of title insurance.</p><br>

					<h3 class="typography-title">How do I obtain Title Insurance?</h3>
					<p>After the escrow officer or lender opens the title order, the title agent or attorney begins a
						title search. A Preliminary Report is issued to the customer for review and approval. All
						closing documents are recorded upon escrow’s instruction. When recording has been confirmed,
						demands are paid, funds are disbursed, and the actual title insurance policy is created.</p><br>

